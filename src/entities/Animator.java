package entities;

public class Animator {

	private String animatorName;

	public Animator() {
	}

	public Animator(String animatorName) {
		super();
		this.animatorName = animatorName;

	}

	public String getAnimatorName() {
		return animatorName;
	}

	public void setAnimatorName(String animatorName) {
		this.animatorName = animatorName;
	}
}
